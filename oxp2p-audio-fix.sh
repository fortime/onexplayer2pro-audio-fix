    hda-verb "$device" 0x20 0x4f1 0x0b >/dev/null 2>&1
    # echo "00280792: hda-verb "$device" 0x20 0x400 0x00"
    hda-verb "$device" 0x20 0x400 0x00 >/dev/null 2>&1
